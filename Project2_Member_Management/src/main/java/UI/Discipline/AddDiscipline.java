    private void jBtnAddVioletActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAddVioletActionPerformed
        // TODO add your handling code here:
        String[] thanhvien = jComboBoxThanhVien.getSelectedItem().toString().split(" - ");
        String maXL = jId.getText().toString();
        String hinhthucxuly = jHinhThucXuLy.getSelectedItem().toString();
        String sotien = jSoTien.getText().toString();
        int trangthaixuly = jTrangThaiXuLy.getSelectedItem().toString().equals("Chưa xử lý") ? 1 : 0;
        long currentTimeMillis = System.currentTimeMillis();
        Date currentDate = new java.util.Date(currentTimeMillis);
        Timestamp timestamp = new Timestamp(currentDate.getTime());
        
        Discipline discipline = new Discipline();
        
        discipline.setDate(timestamp);
        discipline.setStatus(trangthaixuly);
        discipline.setDescription(hinhthucxuly);
        
        if (maXL.matches("\\d+")) {
            int maXLint = Integer.parseInt(maXL);
            if(disciplineBLL.getDiscipline(maXLint) != null) {
                JOptionPane.showMessageDialog(rootPane,"Mã này đã tồn tại, vui lòng nhập mã khác!");
                return;
            }
            else
                discipline.setId(maXLint);
        } 
        else {            
            JOptionPane.showMessageDialog(rootPane,"Lỗi: Mã XL không phải là số!");
            return;
        }
        
        
        if(sotien.isEmpty()){
            discipline.setFine(null);
        }else{
            discipline.setFine(Integer.parseInt(sotien));
        }
        
        Member member = disciplineBLL.getMember(thanhvien[0]);
        discipline.setMemberID(member);
        try{
            disciplineBLL.insertDiscipline(discipline);
            JOptionPane.showMessageDialog(rootPane,"Add Success!");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane,"Add Failed!");
        }
    }//GEN-LAST:event_jBtnAddVioletActionPerformed

